  //datatypes byte //
public class datatypes{
      public static void main(String[] args) {

          //Byte Example
          byte age =22;
          System.out.println(age);


         // short example
          short salary = 3000;
        System.out.println(salary);

        // int example
          int Student =20;
          System.out.println(Student);

          //long example
          long mobileNumber=7297875390l;
          System.out.println(mobileNumber);

          //float example
          float price =99.99f;
          System.out.println(price);


          //double example
          double pi = -4.93247308;
          System.out.println(pi);

          //char example
          char grade ='A';
          System.out.println(grade);

          //boolean example
          boolean myAgeIs22 =true;
          System.out.println(myAgeIs22);
       }
}


